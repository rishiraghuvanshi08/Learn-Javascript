// There are different types of JavaScript operators:

// Arithmetic Operators
// Assignment Operators
// Comparison Operators
// String Operators
// Logical Operators
// Bitwise Operators
// Ternary Operators
// Type Operators


// Arithmetic Operators
let a = 3;
let b = 2;

console.log(a + a);
console.log(a - a);
console.log(a * a);
console.log(a / a);
console.log(a % a);
console.log(a ** a);    // a^a
console.log(a++);
console.log(a--);


// Assignment Operators
let x = 10;
x += 5;
x -= 5;
x *= 5;
x /= 5;
x %= 5;
console.log(x);

// +=	        x += y      	x = x + y
// =	        x = y	        x = y
// -=	        x -= y      	x = x - y
// *=	        x *= y      	x = x * y
// /=	        x /= y      	x = x / y
// %=	        x %= y      	x = x % y
// **=	        x **= y	        x = x ** y


// Comparison Operators
